    public static String getPumpkin(String type){
        String pumpkin;

        switch (type){
            case "Lisa":
                pumpkin = monaLisaPumpkin();
                break;
            case "Cat":
                pumpkin = catPumpkin();
                break;
            case "Toothy":
                pumpkin = toothyPumpkin();
                break;
            default:
                pumpkin = toothyPumpkin();
                break;
        }
        return pumpkin;
    }

    public static List<String> getPumpkinTypes(){
        return Arrays.asList("Lisa", "Cat", "Toothy");
    }


